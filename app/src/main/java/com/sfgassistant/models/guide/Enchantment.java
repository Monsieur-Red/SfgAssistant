public class Enchantment {

    private String  image;
    private String  type;
    private String  name;
    private String  effect;

    public String getImage() {
        return image;
    }

    public String getType() {
        return type;
    }

    public String getName() {
        return name;
    }

    public String getEffect() {
        return effect;
    }
}
